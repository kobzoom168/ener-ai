"""Channel profiles: separate a channel's *identity* (niche, hooks, look, voice, brand)
from the shared retention engine in vdo_agent.

Adding a new channel = add one ChannelProfile here. The engine (script beats, the two
metric optimisers, TTS, subtitles, render, posting) is reused unchanged. Every profile
inherits the universal viral skeleton; only the genre-specific wording differs.

The two metrics the whole system optimises for (per the creator's rule):
  • Stayed-to-watch (must stay >70%) -> driven by the HOOK (first 2s)
  • AVD / average view duration       -> driven by open-loop + pacing + CTA
"""
from __future__ import annotations

from dataclasses import dataclass, field


@dataclass(frozen=True)
class ChannelProfile:
    id: str
    name: str
    # --- script identity -----------------------------------------------------
    persona: str            # who the narrator is (system prompt lead)
    audience: str           # how to address the viewer
    content_rules: str      # genre do/don't (respect beliefs / don't fake numbers …)
    hooks: str              # genre-tuned stop-scroll hook menu (drives Stayed-to-watch)
    beats: str              # genre-tuned beat sheet (drives AVD)
    topic_pick: str         # what to do when no topic is supplied (auto channel)
    fallback_title: str
    # --- research + QC --------------------------------------------------------
    research_mode: str      # "facts" | "belief" | "none"
    research_persona: str
    research_question: str  # "{subject}" placeholder
    qc_system: str
    qc_label: str           # log line shown while QC runs
    # --- voice / pronunciation ----------------------------------------------
    pronun_examples: str    # examples for the spoken (lines_say) re-spelling
    # --- visuals --------------------------------------------------------------
    visual_style: str       # image-prompt guidance
    video_query_hint: str   # stock-video query guidance
    ai_video_hint: str      # hero AI-video scene guidance
    # --- post / brand ---------------------------------------------------------
    caption_hint: str       # hashtag/caption guidance
    brand_handle: str       # watermark handle (green)
    brand_web: str          # watermark site (white)
    face_pip: bool = False  # talking-head PIP allowed for this channel
    default_tone: str = "evidence"
    yt_title_hint: str = ""  # how to write the click-worthy YouTube title for this genre


